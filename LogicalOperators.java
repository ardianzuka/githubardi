package S3;

public class LogicalOperators {

    public static void main(String[] args) {

        boolean p = true;
        boolean q = false;


        System.out.println("p && q: " + !(p && q));
        System.out.println("p || q: " + !(p || q));
        System.out.println("p && q: " + (p && q));



    }
}
